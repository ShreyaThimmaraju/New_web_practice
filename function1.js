function add(a, b) {
    console.log("2 - parameter add function");
    console.log("Sum = " + (a + b));
}

function add(a, b, c) {
    console.log("3 - parameter add function");
    console.log("Sum = " + (a + b + c));
}



function add(a, b, c, d) {
    console.log("4 - parameter add function");
    console.log("Sum = " + (a + b + c +d));
}


add(10, 20);
add(10, 20, 30);
add(10, 20, 30, 40);